    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        
        //getting data in form of string
        String course=(String)jComboBox1.getSelectedItem();
        String branchName=jComboBox2.getSelectedItem().toString();
        String rollNo=jTextField1.getText();
        String name=jTextField2.getText();
        String gender=(String)jComboBox3.getSelectedItem();
        String fatherName=jTextField3.getText();
        
        
        //
        boolean flag=true;
        
        //checking if any cell is empty
        if(rollNo.equals("") || name.equals("") || fatherName.equals(""))
        {
            flag=false;
        }
        
        try
        {
           // connecting to sql database
           Class.forName("com.mysql.cj.jdbc.Driver"); 
           Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srms","root", "Pr@s@d750#");
           Statement st=con.createStatement();
           if(flag)
           {
               
            //updating data in sql  database
            st.executeUpdate("insert into student(rollNO,course,branchName,name,gender,fatherName) values('"+rollNo+"','"+course+"','"+branchName+"','"+name+"','"+gender+"','"+fatherName+"')");    
            JOptionPane.showMessageDialog(null,"Data saved succesfully!");
           }
           else
           {
               JOptionPane.showMessageDialog(null,"Please fill empty field");
           }
           
          //switchig tabs
           setVisible(false);
           new AdminHome().setVisible(true);
           
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_jButton6ActionPerformed

