    private void jButton21ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton21ActionPerformed
        // TODO add your handling code here:
        
        //getting data in string
        String roll=jTextField1.getText();
        String physics=jTextField2.getText();
        String maths=jTextField3.getText();
        String em=jTextField4.getText();
        String dbms=jTextField5.getText();
        String os=jTextField6.getText();
        
        boolean flag=true;
        //checking if any filed  is empyt
        if(roll.equals("") ||physics.equals("") || maths.equals("") || em.equals("")|| dbms.equals("") || os.equals(""))
        {
            flag=false;
        }
         
        try
        {
        Class.forName("com.mysql.cj.jdbc.Driver");
        Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srms", "root", "Pr@s@d750#");
        Statement st=con.createStatement();
        ResultSet rs=st.executeQuery("Select * from student where rollNo ='"+roll+"'");
        
        
        if(rs.next() && flag)
        {
            st.executeUpdate("insert into result(rollNo,physics,maths,em,dbms,os) values('"+roll+"','"+physics+"','"+maths+"','"+em+"','"+dbms+"','"+os+"')");
            JOptionPane.showMessageDialog(null, "Result saved succesfully!");
            setVisible(false);
            new InsertResult().setVisible(true);
        }
        else if(!flag)
        {
            JOptionPane.showMessageDialog(null,"Please fill empty field");
        }
        else
        {
            JOptionPane.showMessageDialog(null,"This Roll No.is Not registerd!");
        }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.toString());
        }
        
    }//GEN-LAST:event_jButton21ActionPerformed

